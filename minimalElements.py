def findMinElems(upset):
    minElems = []

    for s in upset:
        is_minimal = True

        for sPrime in upset:
            if sPrime != s and set(sPrime).issubset(set(s)):
                is_minimal = False
                break

        if is_minimal:
            minElems.append(s)

    return minElems

